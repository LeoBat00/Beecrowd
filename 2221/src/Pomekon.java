public class Pomekon {
    private int ataque;
    private int defesa;

    public Pomekon(){
        ataque = 0;
        defesa = 0;

    }

    public int getAtaque() {
        return ataque;
    }

    public int getDefesa() {
        return defesa;
    }

    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }
    
    public void setDefesa(int defesa) {
        this.defesa = defesa;
    }

}
